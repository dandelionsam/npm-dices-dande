exports.diceThrow = function(sides){
    return Math.floor(Math.random() * (sides + 1)) + 1;
};

exports.diceAvg = function(sides, number){
    var avg = 0;
    for (let i = 0; i < number; i++){
        avg += diceThrow(sides);
    }
    return Math.floor((avg / number));
}

